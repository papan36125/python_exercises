x = 10 # 0000 1010 in binary
y = 4 # 0000 0100 in binary

# Bitwise AND, Output: x& y = 0, 0000 0000 in binary
print('x & y is',x & y)

# Bitwise OR, Output: x | y = 14, 0000 1110 in binary
print('x | y is',x | y)

# Bitwise NOT, Output: ~x = -11, 1111 0101 in binary
print('~x = ',~x)

# Bitwise XOR, Output: x ^ y = 14, 0000 1110 in binary
print('x ^ y is',x ^ y)

# Bitwise right shift, Output: x >> 2 = 2, 0000 0010 in binary
print('x >> 2 is ',x >> 2)

# Bitwise left shift, Output: x << 2 = 40, 0010 1000 in binary
print('x << 2 is',x << 2)
